# mathematical operations

# Addition
print(3 + 5)

# Substraction
print(5 - 3)

# Multiplication
print(8 * 9)

# division will always give you floating point number due to implicit type casting 
print(9 / 3)

# to avoid implicit type casting use (//)
print(9//3)

# Exponentiation
print(5**4)

# Modulus
print(8%3)