# Lists

fruits = ['Apple' , 'Banana' , 'Orange']

print(fruits)
print(fruits[0])
print(fruits[2])
print(fruits[-1])


# changing the element in the list
fruits[2] = "Grapes"

print(fruits)

# adding elements to the end of list 
fruits.append("Pineapple")
print(fruits)

# adding a list to the end of list 
fruits.extend(["Papaya" , "Mango"])

print(fruits)